class Operators {
    public static void main(String[] args) {
        int a = 2;
        int b = 4;
        int result = a * b;
        int result1 = a + b;
        int result2 = a - b;
        int result3 = a % b;

        System.out.println(result);
        System.out.println(result1);
        System.out.println(result2);
        System.out.println(result3);
    }
    
}
