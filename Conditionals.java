class Conditionals {
    public static void main(String[] args) {
        int x = 30;
        int y = 20;
        int z = 18;
        if(x>y && x>z){
            System.out.println("x");
        }
        else if(y>x && y>z){
            System.out.println("y");
        }
        else{
            System.out.println("z");
        }
    }
    
}
